        private void populateTable() {
             try {
            String query = "SELECT * FROM transaksi_pembayaran"; // Ganti 'nama_tabel' dengan nama tabel sesuai database kamu
            Statement stmt = connection.createStatement();
            ResultSet rs = stmt.executeQuery(query);

            while (rs.next()) {
                
                String ID_Transaksi = rs.getString("ID_Transaksi");
                String ID_Pegawai = rs.getString("ID_Pegawai");
                String ID_Pesanan = rs.getString("ID_Pesanan");
                String Metode_Pembayaran = rs.getString("Metode_Pembayaran");
                float Total_Pembayaran = rs.getFloat("Total_Pembayaran");
                String Tanggal_waktu_transaksi = rs.getString("Tanggal_waktu_transaksi");
             
                

                model.addRow(new Object[]{ID_Transaksi, ID_Pegawai, ID_Pesanan, Metode_Pembayaran, Total_Pembayaran, Tanggal_waktu_transaksi});
            }

            rs.close();
            stmt.close();
        } catch (Exception e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Terjadi kesalahan saat mengambil data dari database.");
    }
    }
