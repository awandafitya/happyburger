    private void btnsimpandataActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsimpandataActionPerformed
        // TODO add your handling code here:
         String ID_Transaksi = idtransaksi.getText();
        String ID_Pegawai = idpegawai.getText();
        String ID_Pesanan = idpesanan.getText();
        String Metode_Pembayaran = (String) metodepembayaran.getSelectedItem();
        float Total_Pembayaran = Float.parseFloat(totalpembayaran.getText());
        Date Tanggal_waktu_transaksi = tanggaltransaksi.getDate();
        
        try {
            String query = "INSERT INTO transaksi_pembayaran (id_transaksi, id_pegawai, id_pesanan, metode_pembayaran, total_pembayaran, tanggal_waktu_transaksi) VALUES (?, ?, ?, ?, ?, ?)";
            PreparedStatement pstmt = connection.prepareStatement(query);

            pstmt.setString(1, ID_Transaksi);
            pstmt.setString(2, ID_Pegawai);
            pstmt.setString(3, ID_Pesanan);
            pstmt.setString(4, Metode_Pembayaran);
            pstmt.setFloat(5, Total_Pembayaran);
            pstmt.setDate(6, new java.sql.Date (Tanggal_waktu_transaksi.getTime()));

            pstmt.executeUpdate();
            pstmt.close();

            // Menyegarkan tabel setelah menambahkan data baru
            model.setRowCount(0);
            populateTable();
            
            
        } catch (Exception e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Terjadi kesalahan saat mengambil data dari database.");
        }
   
    }//GEN-LAST:event_btnsimpandataActionPerformed

